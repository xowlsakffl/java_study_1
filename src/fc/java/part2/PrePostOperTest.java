package fc.java.part2;

public class PrePostOperTest {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(--a);//9
        System.out.println(a++);//9
        System.out.println(a--);//10
        System.out.println(++a);//10
        System.out.println(a);//10
    }
}
